def plot_trimesh(mesh: trimesh.Trimesh, title="STL Preview") -> go.Figure:
    v = mesh.vertices
    f = mesh.faces
    fig = go.Figure(
        data=[
            go.Mesh3d(
                x=v[:,0], y=v[:,1], z=v[:,2],
                i=f[:,0], j=f[:,1], k=f[:,2],
                opacity=0.6,
                flatshading=True,
                name="STL",
                lighting=dict(ambient=0.4, diffuse=0.6, specular=0.2),
                lightposition=dict(x=1, y=1, z=1),
                showscale=False,
            )
        ]
    )
    fig.update_layout(
        title=title,
        scene=dict(aspectmode="data"),
        margin=dict(l=0, r=0, t=40, b=0),
    )
    return fig

def plot_paths(polylines: List[np.ndarray], title="Sliced Paths (3D)") -> go.Figure:
    fig = go.Figure()
    for poly in polylines:
        fig.add_trace(
            go.Scatter3d(
                x=poly[:,0], y=poly[:,1], z=poly[:,2],
                mode="lines",
                line=dict(width=3),
                name="path"
            )
        )
    fig.update_layout(
        title=title,
        scene=dict(aspectmode="data"),
        margin=dict(l=0, r=0, t=40, b=0),
        showlegend=False,
    )
    return fig

